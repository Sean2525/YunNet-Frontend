                        <span class="number">（二）</span>擅自截取網路傳輸訊息。
                      </p>
                    </div>
                  </li>
                  <li>
                    <div class="chwithbracket">
                      <p>
                        <span class="number">（三）</span>以破以破解、盜用或冒用他人帳號及密碼等方式，未經授權使用網路資源，或無故洩漏他人之帳號及密碼。解、盜用或冒用他人帳號及密碼等方式，未經授權使用電腦或網路資源。
                      </p>
                    </div>
                  </li>
                  <li>
                    <div class="chwithbracket">
                      <p>
                        <span class="number">（四）</span>無故將帳號借予他人使用。
                      </p>
                    </div>
                  </li>
                  <li>
                    <div class="chwithbracket">
                      <p>
                        <span class="number">（五）</span>隱藏帳號或使用虛假帳號。但經明確授權得匿名使用者不在此限。
                      </p>
                    </div>
                  </li>
                  <li>
                    <div class="chwithbracket">
                      <p>
                        <span class="number">（六）</span>窺視他人之電子郵件或檔案。
                      </p>
                    </div>
                  </li>
                  <li>
                    <div class="chwithbracket">
                      <p>
                        <span class="number">（七）</span>以任何方式濫用網路資源，包括以電子郵件大量傳送廣告信、連鎖信或無用之信息，或以灌爆信箱、掠奪資源等方式，影響系統之正常運作。
                      </p>
                    </div>
                  </li>
                  <li>
                    <div class="chwithbracket">
                      <p>
                        <span class="number">（八）</span>以電子郵件、線上談話、電子佈告欄（BBS ）或類似功能之方法散布詐欺、誹謗、侮辱、猥褻、騷擾、非法軟體交易或其他違法之訊息。
                      </p>
                    </div>
                  </li>
                  <li>
                    <div class="chwithbracket">
                      <p>
                        <span class="number">（九）</span>利用學校之網路資源從事非教學研究等相關之活動或違法行為。
                      </p>
                    </div>
                  </li>
                </ol>
