                  </li>
                  <li>
                    <div class="chwithbracket">
                      <p>
                        <span class="number">（四）</span>BBS 及其他網站應設置專人負責管理、維護。違反網站使用規則者，負責人得刪除其文章或暫停其使用。情節重大、違反校規或法令者，並應轉請學校處置。
                      </p>
                    </div>
                  </li>
                  <li>
                    <div class="chwithbracket">
                      <p>
                        <span class="number">（五）</span>其他有關校園網路管理之事項。
                      </p>
                    </div>
                  </li>
                </ol>
                <p>使用者若發現系統安全有任何缺陷，應儘速報告網路管理單位。</p>
              </div>
            </li>
            <li>
              <div>
                <p>
                  <span class="number">六、</span>網路隱私權之保護
                </p>
                <p>學校應尊重網路隱私權，不得任意窺視使用者之個人資料或有其他侵犯隱私權之行為。但有下列情形之一者，不在此限：</p>
                <ol>
                  <li>
                    <div class="chwithbracket">
                      <p>
                        <span class="number">（一）</span>為維護或檢查系統安全。
                      </p>
                    </div>
                  </li>
                  <li>
                    <div class="chwithbracket">
                      <p>
                        <span class="number">（二）</span>依合理之根據，懷疑有違反校規之情事時，為取得證據或調查不當行為。
                      </p>
                    </div>
                  </li>
                  <li>
                    <div class="chwithbracket">
                      <p>
                        <span class="number">（三）</span>為配合司法機關之調查。
                      </p>
                    </div>
                  </li>
                  <li>
                    <div class="chwithbracket">
                      <p>
                        <span class="number">（四）</span>其他依法令之行為。
                      </p>
                    </div>
                  </li>
                </ol>
              </div>
            </li>
            <li>
              <div>
                <p>
                  <span class="number">七、</span>違反之效果
                </p>
                <p>網路使用者違反本規範者，將受到下列之處分：</p>
                <ol>
                  <li>
                    <div class="chwithbracket">
                      <p>
                        <span class="number">（一）</span>停止使用網路資源。
                      </p>
                    </div>
                  </li>
                  <li>
                    <div class="chwithbracket">
                      <p>
                        <span class="number">（二）</span>接受校規之處分。
                      </p>
                    </div>
                  </li>
                  <li>
                    <div class="chwithbracket">
                      <p>
                        <span class="number">（三）</span>依前兩項規定之處分者，其另有違法行為時，行為人尚應依民法、刑法、著作權法或其他相關法令負法律責任。
                      </p>
                    </div>
                  </li>
                </ol>
                <p>網路管理者違反本規範者，應加重其處分。</p>
                <p>依前兩項規定之處分者，其另有違法行為時，行為人尚應依民法、刑法、著作權法或其他相關法令負法律責任。</p>
              </div>
            </li>
            <li>
              <div>
                <p>
                  <span class="number">八、</span>處理原則及程序
                </p>
                <p>各校訂定之校園網路使用規範應明定於校規。</p>
                <p>前項校規和網路管理單位對違反本規範之行為人，或為防範違反本規範，對行為人或非特定對象所採取之各項管制措施，應符合必要原則、比例原則及法律保留原則。</p>
                <p>各校對違反本規範之行為人所為之處分，應依正當法律程序，提供申訴和救濟機制。</p>
                <p>學校處理相關網路申訴或救濟程序時，應徵詢校內網路委員會或指定專人之意見。</p>
              </div>
            </li>
          </ol>
